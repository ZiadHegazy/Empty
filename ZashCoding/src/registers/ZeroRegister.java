package registers;

public class ZeroRegister {
	private final String binaryValue ="00000000000000000000000000000000"; 
	private final int DecimalValue=0;
	public ZeroRegister(){
		
	}
	public String getBinaryValue() {
		return binaryValue;
	}
	public int getDecimalValue() {
		return DecimalValue;
	}
}
